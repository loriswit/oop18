package project09.gfx;

public class Vector
{
    private double dx;
    private double dy;
    
    public Vector(double dx, double dy)
    {
        this.dx = dx;
        this.dy = dy;
    }
    
    public double dx()
    {
        return dx;
    }
    
    public double dy()
    {
        return dy;
    }
}
